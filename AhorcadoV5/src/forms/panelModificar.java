    private void completaTodo()
    {
        int index = this.comboPalabras1.getSelectedIndex();
        Palabra palabra1 = this.diccionario.getLista().get(index);
        this.txtPista.setText(palabra1.getPista());
        this.comboDificil.setSelectedIndex(palabra1.getDificultad());
    }

